  @doc """
  Returns the list of songs.

  ## Examples

      iex> list_songs()
      [%Song{}, ...]

  """
  def list_songs do
    Repo.all(Song)
  end
  def list_songs_with_info do
    Repo.all(song_with_details_query()) 
  end

  @doc """
  Gets a single song.

  Raises `Ecto.NoResultsError` if the Song does not exist.

  ## Examples

      iex> get_song!(123)
      %Song{}

      iex> get_song!(456)
      ** (Ecto.NoResultsError)

  """
  def get_song!(id), do: Repo.get!(Song, id)
  def get_song(id), do: Repo.get(Song, id)
  def song_with_details_query do
    from s in Song,
      join: a in Album,
      join: ar in Artist,
      on: a.id == s.album_id and a.artist_id == ar.id,
      select: %{song: s, album_name: a.name, cover: a.cover, artist: ar.name}
  end
  def song_with_details_query(id) do
    from [s, x] in song_with_details_query(),
      where: s.id == ^id
  end
  def get_song_with_album_details!(%Song{} = song) do
    song_with_details_query(song.id)
    |> Repo.one!()
  end
  def get_song_with_album_details!(id) do
    song_with_details_query(id)
    |> Repo.one!()
  end

  @doc """
  Creates a song.

  ## Examples

      iex> create_song(%{field: value})
      {:ok, %Song{}}

      iex> create_song(%{field: bad_value})
      {:error, %Ecto.Changeset{}}

  """
